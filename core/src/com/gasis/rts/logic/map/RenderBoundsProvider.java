package com.gasis.rts.logic.map;

/**
 * Provides render bounds
 */
public interface RenderBoundsProvider {

    /**
     * Gets render x
     *
     * @return
     */
    float getRenderX();

    /**
     * Gets render y
     *
     * @return
     */
    float getRenderY();

    /**
     * Gets render width
     *
     * @return
     */
    float getRenderWidth();

    /**
     * Gets render height
     *
     * @return
     */
    float getRenderHeight();
}
